import random

# 获取输入（虽然你的代码里没用到变量 a，但保留了这一行）
a=[ 0.859 , 0.250 , 0.503 , 0.592 , 0.606 , 0.604 , 0.652 , 0.308 , 0.765 , 0.337 , 0.645 , 0.324 , 0.801 , 0.353 , 0.756 ]
for i in a:
    # 使用 uniform 生成指定范围内的随机浮点数
    r = random.uniform(-1.13, 1.13)
    t = random.uniform(-1.13, 1.13)
    print(i*(1+r/100),i*(1+t/100))


